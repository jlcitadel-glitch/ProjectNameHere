/// <summary>
/// Categories for innate character abilities beyond the skill tree.
/// These represent progression unlocks, traversal, combat, and utility powers.
/// </summary>
public enum AbilityCategory
{
    // Core progression systems
    Progression = 0,

    // Movement and traversal
    Traversal = 1,

    // Combat abilities that also enable traversal/exploration
    CombatTraversal = 2,

    // Environmental interaction
    Environmental = 3,

    // Defensive and survival
    Defensive = 4,

    // Utility and quality-of-life
    Utility = 5
}
